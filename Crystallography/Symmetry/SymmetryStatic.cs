    // 両者は同一インデックスの「対称操作の文字列形 / アフィン変換形」。CSV(x,y,z 標準表記)から
    // 文字列はそのまま、生成器は AffineGen.Parse で構築する。CSV 行は座標式に , や " を含まない (区切りの , のみ)。
    private static class PositionData
    {
        public static readonly string[] Strings;
        public static readonly AffineGen[] Generators;
        static PositionData()
        {
            using var r = OpenSymCsv("PositionStringList"); // 260605Cl リソース展開を OpenSymCsv ヘルパーに集約 (旧: GetManifestResourceStream インライン再実装)
            var strs = new System.Collections.Generic.List<string>(2000);
            var gens = new System.Collections.Generic.List<AffineGen>(2000);
            for (string line; (line = r.ReadLine()) != null;)
            {
                if (line.Length == 0 || line[0] == '#') continue; // 空行 / コメント(##・列見出し)をスキップ
                var p = line.Split(',');                          // 座標式にカンマは含まれない → 3 成分
                strs.Add(line);                                   // 行そのものが元の "x,y,z" 文字列
                gens.Add(AffineGen.Parse(p[0], p[1], p[2]));
            }
            Strings = [.. strs];
            Generators = [.. gens];
        }
    }
